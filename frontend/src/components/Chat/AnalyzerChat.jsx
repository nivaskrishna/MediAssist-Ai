const BACKEND =
  import.meta.env.VITE_API_URL?.replace("/api", "") || "http://localhost:8000";

/**
 * InlineStepCard – renders one first-aid step with an AI image beside it.
 *
 * Strategy (in priority order):
 *  1. If step.image_url is already set by the backend analyze endpoint
 *     (a path like /api/analyze/image-file/{hash}), resolve it to a full
 *     URL and display it immediately — no extra network call needed.
 *  2. If step.image_url is missing or fails, fall back to calling
 *     /api/image-gen/generate (which proxies Pollinations internally).
 *     Requests are staggered by step number to avoid 429 rate limits.
 */
const InlineStepCard = ({ step, condition }) => {
  const [imgSrc, setImgSrc] = useState(null);
  const [busy, setBusy] = useState(false);
  const [errMsg, setErrMsg] = useState(null);
  const hasFetched = useRef(false);

  // Resolve the backend-provided image URL to a full absolute URL
  const resolveBackendUrl = (url) => {
    if (!url) return null;
    if (url.startsWith("http")) return url; // Already absolute
    if (url.startsWith("/api/") || url.startsWith("/api")) {
      return `${BACKEND}${url}`; // Relative backend path
    }
    return url;
  };

  const fetchViaGenerateApi = useCallback(async () => {
    if (busy) return;
    setBusy(true);
    setErrMsg(null);
    setImgSrc(null);

    const prompt =
      `3D animated educational medical illustration. ` +
      `Show a person correctly performing this first aid action: ${step.step_text}. ` +
      `Pixar style. Healthcare training animation. Bright colors. ` +
      `Clear action demonstration. No text overlay. High quality.`;

    console.log(
      `[InlineStepCard] Step ${step.step_number}: calling /api/image-gen/generate…`,
    );

    // Stagger: each step waits (step_number-1) * 1.2s to avoid 429 rate limits
    const delay = (step.step_number - 1) * 1200;
    if (delay > 0) await new Promise((r) => setTimeout(r, delay));

    try {
      const data = await generateSDXLImage(prompt);
      console.log(
        `[InlineStepCard] Step ${step.step_number}: OK — length=${data.image_url?.length ?? 0}`,
      );
      if (!data.image_url) throw new Error("Backend returned empty image_url");
      setImgSrc(data.image_url);
    } catch (e) {
      const msg =
        e?.response?.data?.detail || e?.message || "Generation failed";
      console.error(`[InlineStepCard] Step ${step.step_number} error:`, msg);
      setErrMsg(msg);
    } finally {
      setBusy(false);
    }
  }, [step.step_text, step.step_number]);

  useEffect(() => {
    if (hasFetched.current) return;
    hasFetched.current = true;

    const prebuilt = resolveBackendUrl(step.image_url);
    if (prebuilt) {
      // Path 1: use the image already generated by the analyze endpoint
      console.log(
        `[InlineStepCard] Step ${step.step_number}: using pre-built URL: ${prebuilt.slice(0, 80)}`,
      );
      setImgSrc(prebuilt);
    } else {
      // Path 2: no pre-built image, call /api/image-gen/generate
      fetchViaGenerateApi();
    }
  }, []);

  const handleRetry = useCallback(() => {
    hasFetched.current = false;
    setErrMsg(null);
    setImgSrc(null);
    setBusy(false);
    // Small delay so React re-renders before we start
    setTimeout(() => {
      hasFetched.current = true;
      fetchViaGenerateApi();
    }, 100);
  }, [fetchViaGenerateApi]);

  return (
    <div className="flex flex-col sm:flex-row items-stretch sm:items-start gap-4 p-4 rounded-xl bg-slate-950/30 border border-slate-800/50 hover:border-slate-700/60 transition-all duration-200">
      {/* Step number badge */}
      <div className="shrink-0 flex items-center gap-2 sm:block">
        <div className="w-8 h-8 rounded-full bg-blue-600/15 border border-blue-500/30 flex items-center justify-center">
          <span className="text-xs font-extrabold text-blue-400">
            {step.step_number}
          </span>
        </div>
        <span className="sm:hidden text-xs font-bold text-slate-400">
          Step {step.step_number}
        </span>
      </div>

      {/* Image area */}
      <div className="w-full sm:w-64 h-44 sm:h-40 rounded-lg overflow-hidden border border-slate-800/60 bg-slate-950 flex items-center justify-center relative shrink-0 group">
        {/* Loading spinner */}
        {busy && (
          <div className="absolute inset-0 flex flex-col items-center justify-center gap-1.5 bg-slate-950 z-10">
            <div className="w-6 h-6 rounded-full border-2 border-violet-500/30 border-t-violet-400 animate-spin" />
            <p className="text-[9px] text-slate-500 font-bold">Generating…</p>
          </div>
        )}

        {/* Error state */}
        {errMsg && !busy && (
          <div className="absolute inset-0 flex flex-col items-center justify-center gap-1.5 z-10 bg-slate-950 p-2">
            <p className="text-[9px] text-rose-450 text-center leading-tight line-clamp-2">
              {errMsg.length > 40 ? "Generation failed" : errMsg}
            </p>
            <button
              onClick={handleRetry}
              className="text-[9px] text-rose-450 hover:text-rose-350 flex items-center gap-0.5 font-bold mt-1"
            >
              <RefreshCw className="h-3 w-3 animate-spin-hover" /> Retry
            </button>
          </div>
        )}

        {/* Loaded image */}
        {imgSrc && !busy && (
          <img
            src={imgSrc}
            alt={`First aid step ${step.step_number}`}
            className="w-full h-full object-cover transition-transform duration-500 group-hover:scale-110"
            onLoad={() => {
              console.log(
                `[InlineStepCard] Step ${step.step_number}: image rendered ✓`,
              );
            }}
            onError={(e) => {
              console.warn(
                `[InlineStepCard] Step ${step.step_number}: img onError — falling back to generate API`,
              );
              e.target.style.display = "none";
              setImgSrc(null);
              // If the pre-built URL failed, fall back to the generate API
              if (!errMsg) fetchViaGenerateApi();
            }}
          />
        )}
      </div>

      {/* Step text */}
      <div className="flex-1 min-w-0 pt-0.5">
        <p className="text-[14px] sm:text-base text-slate-100 font-semibold leading-relaxed">
          {step.step_text}
        </p>
      </div>
    </div>
  );
};

